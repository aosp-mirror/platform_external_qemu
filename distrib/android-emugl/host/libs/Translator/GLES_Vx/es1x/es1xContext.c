/*---------------------------------------------------------------------------*/

void *es1xContext_create()
{
  ES1X_ASSERT(gl2b != NULL);

  es1xContext* context = (es1xContext*) es1xMalloc(sizeof(es1xContext));
  int i=0;

  /*------------------------------------------------------------------------
   * Matrix stacks
   *----------------------------------------------------------------------*/

  context->projectionMatrixStack = es1xMatrixStack_create(ES1X_PROJECTION_MATRIX_STACK_SIZE);
  context->modelViewMatrixStack  = es1xMatrixStack_create(ES1X_MODELVIEW_MATRIX_STACK_SIZE);

  for (i=0;i<ES1X_NUM_SUPPORTED_TEXTURE_UNITS;i++)
    context->textureMatrixStack[i] = es1xMatrixStack_create(ES1X_TEXTURE_MATRIX_STACK_SIZE);
  context->currentMatrixStack = context->modelViewMatrixStack;

  /*------------------------------------------------------------------------
   * Special initialization for lights. Diffuse and specular for first
   * light have special initialization values so they need to be handled
   * manually here.
   *----------------------------------------------------------------------*/

  es1xLight_init(&context->light[0]);
  context->lightType[0] = es1xLight_determineType(&context->light[0]);

  es1xColor_set(&context->light[0].diffuse,  1.0f, 1.0f, 1.0f, 1.0f);
  es1xColor_set(&context->light[0].specular, 1.0f, 1.0f, 1.0f, 1.0f);
  context->lightEnabled[0] = GL_FALSE;

  for(i=1;i<ES1X_NUM_SUPPORTED_LIGHTS;i++)
  {
    es1xLight_init(&context->light[i]);
    context->lightType[i]       = es1xLight_determineType(&context->light[i]);
    context->lightEnabled[i]    = GL_FALSE;
  }

  /*------------------------------------------------------------------------
   * Texturing
   *----------------------------------------------------------------------*/

  for(i=0;i<ES1X_NUM_SUPPORTED_TEXTURE_UNITS;i++)
  {
    es1xTexEnv_init(&context->texEnv[i]);
    context->texture[i].id              = 0;
    context->texture[i].format  = ES1X_TEXTUREFORMAT_RGBA;
    context->boundTexture[i]    = &context->texture[i];
  }

  context->textureHashTable = es1xTextureHashTable_create();
  context->textureAllocator = es1xTextureAllocator_create(ES1X_NUM_SUPPORTED_TEXTURE_UNITS);

  /*------------------------------------------------------------------------
   * Shaders
   *----------------------------------------------------------------------*/

  context->shaderCache = es1xShaderCache_create();

  /*------------------------------------------------------------------------
   * Initialize autogenerated parameters
   *----------------------------------------------------------------------*/

#include "es1xContextInit.inl"

  /*------------------------------------------------------------------------
   * Unsorted attributes
   *----------------------------------------------------------------------*/

  context->dirty                        = GL_TRUE;
  context->lastError                    = GL_NO_ERROR;
  context->lastUsedShaderProgramInfo    = 0;
  context->activeBufferBinding          = 0;

  es1xUniformVersions_init(&context->uniformVersions);

#ifdef ES1X_SUPPORT_STATISTICS
  es1xStatistics_clear(&context->statistics);
#endif

  fprintf(stdout, "*** es1xContext_create @ 0x%p \n", context);

  return (void *) context;
}

/*---------------------------------------------------------------------------*/

void es1xContext_destroy(void *_context_)
{
  es1xContext* context = (es1xContext *) _context_;
  int i=0;

  ES1X_ASSERT(context);

  /* Destroy matrix stacks */
  es1xMatrixStack_destroy(context->projectionMatrixStack);
  es1xMatrixStack_destroy(context->modelViewMatrixStack);
  for(i=0;i<ES1X_NUM_SUPPORTED_TEXTURE_UNITS;i++)
    es1xMatrixStack_destroy(context->textureMatrixStack[i]);
  context->currentMatrixStack = ES1X_NULL;

  /* Destroy shader cache */
  es1xShaderCache_destroy(context->shaderCache);
  context->shaderCache = ES1X_NULL;

  /* Autogenerated destroy routines */
#include "es1xContextDestroy.inl"
}

/*---------------------------------------------------------------------------*/

es1xTexture2D* es1xContext_allocateTexture(es1xContext* context, GLuint id)
{
  if (id == 0)
    return 0;

  ES1X_ASSERT(context);

  /* Allocate and store */
  {
    /* Try to allocate from pool */
    es1xTexture2D* texture = es1xTextureAllocator_allocate(context->textureAllocator);

    /* Allocate from heap if pool is empty */
    if (texture == 0)
      texture = (es1xTexture2D*) es1xMalloc(sizeof(es1xTexture2D));

    ES1X_ASSERT(texture);
    texture->id         = id;
    texture->format     = ES1X_TEXTUREFORMAT_RGBA;

    es1xTextureHashTable_insert(context->textureHashTable, texture->id, texture);
    return texture;
  }
}

/*---------------------------------------------------------------------------*/

es1xTexture2D* es1xContext_getTexture(es1xContext* context, GLuint id)
{
  ES1X_ASSERT(context);

  if (id == 0)
    return 0;

  return es1xTextureHashTable_get(context->textureHashTable, id);
}

/*---------------------------------------------------------------------------*/

void es1xContext_releaseTexture(es1xContext* context, es1xTexture2D* texture)
{
  ES1X_ASSERT(context);
  ES1X_ASSERT(texture);

  if (texture->id == 0)
    return;

  es1xTextureHashTable_remove(context->textureHashTable, texture->id);

  /* Deallocate texture object */
  if (es1xTextureAllocator_contains(context->textureAllocator, texture))
    es1xTextureAllocator_release(context->textureAllocator, texture);
  else
    es1xFree(texture);
}
