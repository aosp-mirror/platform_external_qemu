typedef struct _es1xContext_
{
  /* Matrix stacks */
  es1xMatrixStack*              projectionMatrixStack;
  es1xMatrixStack*              modelViewMatrixStack;
  es1xMatrixStack*              currentMatrixStack;
  es1xMatrixStack*              textureMatrixStack[ES1X_NUM_SUPPORTED_TEXTURE_UNITS];

  /* Clip planes */
  GLboolean                     clipPlaneEnabled[ES1X_NUM_SUPPORTED_CLIP_PLANES];

  /* Texturing */
  es1xTextureAllocator* textureAllocator;
  es1xTextureHashTable* textureHashTable;

  /* Error handling */
  GLenum                        lastError;

  /* Dirty flags */
  GLuint                        dirty;
  es1xShaderProgramInfo*        lastUsedShaderProgramInfo;

  /* Shader caching */
  es1xShaderCache*              shaderCache;
  es1xUniformVersions           uniformVersions;
  es1xIndexMapTable             currentMapTable;

  /* Unsorted */
  GLuint                        activeBufferBinding;

  /* Autogenerated fields */
#include "es1xContextFields.inl"

  /* Statistics */
  es1xStatistics                statistics;

} es1xContext;



