#define ES1X_UPDATE_CONTEXT_STATE(STATEMENT)    \
  {                                             \
    STATEMENT;                                  \
    context->dirty = GL_TRUE;                   \
  }

#define ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, GROUPTYPE)     \
  {                                                                     \
    STATEMENT;                                                          \
    es1xUniformVersions_increment(&context->uniformVersions, GROUPTYPE); \
  }

#define ES1X_UPDATE_CONTEXT_GENERIC_ARRAY_PARAMETER(STATEMENT, ARRAYINDEX, MININDEX, MAXINDEXPLUSONE, GROUPTYPE) \
  {                                                                     \
    STATEMENT;                                                          \
    ES1X_ASSERT(ARRAYINDEX >= MININDEX && ARRAYINDEX < MAXINDEXPLUSONE); \
    es1xUniformVersions_increment(&context->uniformVersions, GROUPTYPE + ARRAYINDEX); \
  }

#define ES1X_UPDATE_CONTEXT_VERTEX_PARAMETER(STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, UNIFORM_GROUP_VERTEX_PARAMETER)
#define ES1X_UPDATE_CONTEXT_POINT_SIZE_PARAMETER(STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, UNIFORM_GROUP_POINT_SIZE)
#define ES1X_UPDATE_CONTEXT_ALPHA_TEST_PARAMETER(STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, UNIFORM_GROUP_ALPHA_TEST)
#define ES1X_UPDATE_CONTEXT_MODELVIEW_MATRIX(STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, UNIFORM_GROUP_MODELVIEW_MATRIX)
#define ES1X_UPDATE_CONTEXT_INVERSION_MODELVIEW_MATRIX(STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, UNIFORM_GROUP_INVERSION_MODELVIEW_MATRIX)
#define ES1X_UPDATE_CONTEXT_PROJECTION_MODELVIEW_MATRIX(STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, UNIFORM_GROUP_PMV_MATRIX)
#define ES1X_UPDATE_CONTEXT_FOG_PARAMETER(STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, UNIFORM_GROUP_FOG)
#define ES1X_UPDATE_CONTEXT_MATERIAL_PARAMETER(STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, UNIFORM_GROUP_MATERIALS)
#define ES1X_UPDATE_CONTEXT_LIGHT_MODEL_PARAMETER(STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, UNIFORM_GROUP_LIGHT_MODEL)
#define ES1X_UPDATE_CONTEXT_UNSORTED_PARAMETER(STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_PARAMETER(STATEMENT, UNIFORM_GROUP_UNSORTED) ;
#define ES1X_UPDATE_CONTEXT_TEXTURE_MATRIX(TEXTURE, STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_ARRAY_PARAMETER(STATEMENT, TEXTURE, 0, ES1X_NUM_SUPPORTED_TEXTURE_UNITS, UNIFORM_GROUP_TEXTURE_MATRIX)
#define ES1X_UPDATE_CONTEXT_LIGHT_PARAMETER(LIGHT, STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_ARRAY_PARAMETER(STATEMENT, LIGHT, 0, ES1X_NUM_SUPPORTED_LIGHTS, UNIFORM_GROUP_LIGHT)
#define ES1X_UDPATE_CONTEXT_CLIP_PLANE_PARAMETER(CLIPPLANE, STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_ARRAY_PARAMETER(STATEMENT, CLIPPLANE, 0, ES1X_NUM_SUPPORTED_CLIP_PLANES, UNIFORM_GROUP_CLIP_PLANE)
#define ES1X_UPDATE_CONTEXT_TEXTURE_ENV_PARAMETER(TEXTUREUNIT, STATEMENT) ES1X_UPDATE_CONTEXT_GENERIC_ARRAY_PARAMETER(STATEMENT, TEXTUREUNIT, 0, ES1X_NUM_SUPPORTED_TEXTURE_UNITS, UNIFORM_GROUP_TEXTURE_ENV)

#define ES1X_CHECK_CONTEXT(CONTEXT)             \
  if(CONTEXT == 0)                              \
    return;

#define ES1X_CHECK_CONTEXT_RET_FALSE(CONTEXT)   \
